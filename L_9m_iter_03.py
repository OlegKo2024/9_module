def fib(n):
    fib_list = []
    a, b = 0, 1
    for _ in range(n):
        fib_list.append(a)
        a, b = b, a + b     # a станет b, а b станет a + b
# проходе цикла a (который теперь равен 1) будет добавлен в fib_list, затем обновления снова произойдут, и значения
# будут изменены: - a станет 1 (текущим b), а b станет 2 (предыдущее a + текущее b)

    return fib_list

result = fib(10)
