# Когда вы начинаете итерацию по fibi_1 с помощью for i in fibi_1:, происходит следующее:
# - На каждой итерации выполняется часть кода генератора, начиная с места, где он был приостановлен (то есть от
# последнего yield).
# - Сначала генератор возвращает 0 (значение a), затем приостанавливает выполнение функции. Значение 0 присваивается
# переменной i в контексте цикла for.
# - Затем происходит вывод print(i, end=' '). В этом случае i получает значение 0, которое выводится на экран.
# - На следующей итерации генератор продолжается с того места, где он был приостановлен, возвращает 1, затем 1,
# затем 2, затем 3, затем 5 и т.д., обновляя значение a в каждой итерации.

# Объект генератора: Когда вы пишете fibi_1 = fib_gen(10), вы создаете объект генератора. Этот объект сохраняет
# состояние функции и позволяет итерироваться по ней, возвращая одно значение за раз, когда это требуется. Объект
# fibi_1 не равен всем числам Фибоначчи сразу; он предоставляет одно значение при каждом обращении.

# Результирование выполнения функции
# не представляет все полученные результаты (в данном случае — числа Фибоначчи) одновременно. Вместо этого он
# представляет механизм, который позволяет извлекать значения на каждом этапе итерирования, и ваша переменная
# fibi_1 является лишь указателем на этот механизм.
# - Вы можете думать о своем генераторе как о функции, которая ведет себя как итератор, возвращая результат по одному
# значению за раз. В отличие от обычной функции, которая могла бы вернуть кортеж или список с результатом всех
# ычислений (например, return [0, 1, 1, 2, 3, 5,...]), генератор возвращает одно значение, когда к нему обращаются.

