def gen_fun(n):
    i = 0
    while i != n:
        yield i
# Когда говорит, что yield i "возвращает текущее значение i", это означает:
# - Возврат значения в итератор: Значение i становится доступным для итератора, который использует генератор.
# Например, в вашем случае, когда вы используете цикл for, он получает активное значение, возвращенное yield, и
# присваивает его переменной i в этом цикле.
# Когда мы говорим, что функция "приостанавливается" после оператора yield, это означает, что выполнение функции
# временно останавливается на этой строке, и управление передается обратно в контекст, где была вызвана функция
# (например, в цикл for)
        i += 1

obj = gen_fun(10)       # Создание генератора для вывода чисел от 0 до 9
print(obj)              # Вывод информации о генераторе
for i in obj:           # # Итерация по генератору
    print(i, end=' ')

# Сравнение yield и return
# 1. Оператор return:
# - Когда в функции используется return, функция завершается сразу после вызова этого оператора. Она возвращает одно
# значение (или несколько, если возвращается кортеж) и полностью завершает свое выполнение.
# - После return функция не может быть вызвана повторно, и все её локальные переменные теряются.
# 2. Оператор yield:
# - Когда выполняется yield, функция возвращает значение (в данном случае, текущее значение переменной i) и
# приостанавливает свое выполнение, сохраняя состояние всех локальных переменных. Это значит, что при следующем вызове
# функции (после итерации) она продолжит выполнение с того места, где была остановлена.
# - Генератори, использующие yield, могут быть вызваны многократно и возвращать новое значение на каждой итерации.

