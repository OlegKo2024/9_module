def fib_gen(n):
    a, b = 0, 1
    for _ in range(n):
# Переменная, фактически отслеживающая количество итераций, отсутствует как таковая; количество итераций управляется
# самим циклом for внутри генератора.
# - Когда for _ in range(n): завершает работу, генератор перестает выдавать новые значения и указатель переходит в
# состояние завершения.
# - Объект генератора может заново вызываться, но значения будут выдаваться в соответствии с порядком, описанным в
# теле генератора.
# Таким образом, сами итерации управляются циклом for в вашем генераторе, а не отдельной переменной для отслеживания
        yield a
        a, b = b, a + b

