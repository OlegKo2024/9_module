print('Decorator - если надо что-то стандартное выполнить внутри какой-то функции(ий) -  например, время выполнения')
# один и тот же декоратор можем применяться к разным функциям - он стандартный и считает например одну из характеристик

import time
import sys
def time_check(func):               # Это функция-декоратор, которая принимает другую функцию (func) в качестве аргумента
    def wrapper(*args, **kwargs):   # 3.Это внутренняя функция, которая добавляет дополнительную логику (например, замер
                                    # времени выполнения)
        start_time = time.time()    # 6.Фиксируем начальное время

        result = func(*args, **kwargs)  # 7 и 12 Вызываем func с теми же аргументами (*args, **kwargs), которые
                                    # были переданы в wrapper
                                    # Выполнение функции digits:
                                    # - Внутри wrapper, вызывается result = func(*args, **kwargs), что фактически
                                    # является result = digits(100, 200, 300).
                                    # - В digits, на каждом числе выполняется операция number ** 5000, и
                                    # результаты суммируются в переменной total.
        end_time = time.time()      # 13.
        elapsed_time = round(end_time - start_time, 4)  # 14.
        print(f'Время: {elapsed_time}') # 15. печатаю время
        return result               # 16. с этим шагом я возвращаю результат - длина нашей строчки
    return wrapper                  # 4.возвращаем нашу функцию

@time_check             # 2.Когда мы используем @time_check, Python автоматически заменяет digits на результат выполнения
                        # time_check(digits), т.е., digits становится функцией wrapper, и её поведение теперь включает
                        # замер времени выполнения
def digits(*args):      # 1.
    total = 1           # 8.
    for number in args: # 9.
        total += number * 5 # 10.
    return len(str(total))  # 11.

sys.set_int_max_str_digits(100000)  # sys.set_int_max_str_digits(100000)` — это команда, использующаяся для изменения
# ограничения на максимально допустимое количество цифр, которые могут находиться в строковом представлении целого числа
# в Python (начиная с версии 3.10). Давайте посмотрим, почему этот вызов может быть уместен в вашем коде, особенно в
# онтексте использования функции `digits`.

result = digits(100, 200, 300)  # 5.Здесь мы вызываем функцию digits с тремя аргументами: 100, 200 и 300.
# Эти аргументы передаются в wrapper как *args, и таким образом, args в wrapper будет равен (100, 200, 300)
print(result)           # 17.



