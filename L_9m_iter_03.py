class Fibonacci:
    def __init__(self, n):
        self.i, self.a, self.b, self.n = 0, 1, 1, n

    def __iter__(self):
        self.i, self.a, self.b = 0, 1, 1
        return self

    def __next__(self):
        self.i += 1
        if self.i > 1:
            if self.i > self.n:
                raise StopIteration()
            self.a, self.b = self.b, self.a + self.b
        return self.a

fib_iter = Fibonacci(20)
print(fib_iter)
for i in fib_iter:
    print(i, end=' ')

